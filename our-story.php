										</strong>
										<p>
											Aatmaja was born into a &quot;broken family"&#44; where she saw her father&#44; an alcoholic&#44; abuse her mother everyday.  Her mother took her anger and frustration out on Aatmaja&#44; telling her that she was &quot;an unwanted child and should have died the day she was born." Shortly after her menstruation started&#44; her mother arranged her marriage with a man who raped and abused her all night. &quot;I was crying out and shouting with pain but he said&#44; &quot;This is the only reason why I married you and you are meant for this." He tried stuffing stones in my vagina. He was psycho."
 One day Aatmaja had enough abuse and left her husbands house to go and live with her mother.  She started teaching tailoring to a girl who lived far away.  The two auto drivers&#44; who became her friends&#44; told her that they could get her a job in Delhi&#44; teaching tailoring for 10&#44;000 rupees a month. She decided to take the job because she &quot;was seeing her mother suffer everyday; and she wanted to do something that could help."
 Next thing she knew&#44; she was at G.B. Road&#44; one of the largest brothel areas of New Delhi.  She was then beaten and kept without food or water. Her first customer was a huge man who had a cynical smile.  She says &quot;how can I ever forget that dreadful night? My hands were tied to the bed&#44; two people parted my legs and held them and the man raped me. He tore my body apart."  The customer then paid the brothel owner 5&#44;000 rupees and Aatmaja was given 50 rupees.
 Aatmaja says that after that day her life became a routine&#44; having sex with many customers a day. One day a customer came that all the other girls refused to have sex with.  The brothel owner made Aatmaja have sex with him without a condom. Later she heard the other girls saying he had a serious medical problem.  She asked the brothel owner about it and she said&#44; &quot;Why do you have to know this. Your life ends here&#44; so why think about living longer."
 One day an English man came to the brothel. He was sent to Aatmaja as a customer&#44; but he didn't even touch her. He was trying to talk to her&#44; but she couldn't understand&#44; but she knew he was a kind man.  The next week a man came to the brothel and took her away.  She wasn't sure what was happening but soon realize that she had been rescued. She was taken to the STOP family home where she met many other young girls like her.
 One day&#44; Aatmaja felt severe stomach pains and was taken to the hospital where she had kidney stones removed. After taking a blood test she found out that she was HIV+.  She says that she doesn't know how long she has to live&#44; but is trying to make the best of each day&#44; each moment that she is alive. Today she doesn't feel ashamed to reveal that she is HIV+ and has learned to fight the trauma and disease.  Now she has a mission&#44; to work for other people with HIV/AIDS. Aatmaja says she is living a fulfilling life now.

