														<p>
															Simply put, there are four main differences between human trafficking and migrant smuggling.<br>
1.	Consent - migrant smuggling, while often undertaken in dangerous or degrading conditions, involves consent. Trafficking victims, on the other hand, have either never consented or if they initially consented, that consent has been rendered meaningless by the coercive, deceptive or abusive action of the traffickers.<br>
2.	Exploitation - migrant smuggling ends with the migrants' arrival at their destination, whereas trafficking involves the ongoing exploitation of the victim.<br>
3.	Transnationality - smuggling is always transnational, whereas trafficking may not be. Trafficking can occur regardless of whether victims are taken to another state or moved within a state's borders.<br>
4.	Source of profits - in smuggling cases profits are derived from the transportation of facilitation of the illegal entry or stay of a person into another county, while in trafficking cases profits are derived from exploitation.<br>
The distinctions between smuggling and trafficking are often very subtle and sometimes they overlap. Identifying whether a case is one of human trafficking or migrant smuggling and related crimes can be very difficult for a number of reasons:<br>
Traffickers may present an 'opportunity' that sounds more like smuggling to potential victims. They could be asked to pay a fee in common with other people who are smuggled. However, the intention of the trafficker from the outset is the exploitation of the victim.

