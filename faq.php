The definition on trafficking consists of three core elements:<br>
1) The action of trafficking which means the recruitment, transportation, transfer, harboring or receipt of persons<br>
2) The means of trafficking which includes threat of or use of force, deception, coercion, abuse of power or position of vulnerability<br>
