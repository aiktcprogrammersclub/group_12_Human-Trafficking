												Prema's Story  
											
										</strong>
										<p>
											Prema was born into a poor family in a small village in the district of Dumka&#44; Jharkhand.  Her father died when she was very young and all  of the responsibility for the family fell on her mother. All they could manage to eat was wild roots and &quot;to have one proper meal seemed impossible".  At a young age&#44; Prema began working as a housemaid&#44; but fell sick and had to quit.  She says &quot;again&#44; the wheel of hunger and starvation began to take toll on our lives". Prema says&#44; one day they didn't have an ounce of food and she tried to fill her stomach on water. Angry and frustrated she decided to leave.
 
Prema went to the nearest railway station and boarded a train to Patna and then to Kolkata.  She didn't know where she was going&#44; or what she was going to do.  When she arrived in Kolkata&#44; she spent the whole day sitting at the platform. The next day a couple approached her and asked her what she was doing.  They invited her to their house to work.  After a month of cleaning&#44; cooking&#44; and washing&#44; they said they would take her to Delhi&#44; to work at their sister's house where she would make more money. She agreed because she desperately wanted to make more money to send back home to her mother and brother.
 The couple took her to Delhi and asked her to wait at the train station.  A few hours later a lady came up to Prema and said that she was their sister. The lady then took Prema to G.B. road and told her that this is where she would work.  Prema asked what kind of work and the lady told her she would find out soon. Prema was then sold to a brothel and was told she was to have sex with men.  When she cried and refused&#44; the brothel owner said that she would have to pay her the money that she was bought for right there and then.  Prema did not have the money.  She was then sent to a room with a man who hit her and raped her mercilessly.
 She says that &quot;After that day&#44; I was a different person. I did not believe in anything in anyone.  I was raped and beaten up almost everyday&#44; every moment."  Prema soon became pregnant and says she did not want the child and hated it even before it was born. She says she &quot;was just a body with a dead soul."
 Prema was in the brothel for a year and a half before she was rescued by STOP India.  She was taken to a government shelter home where she gave birth to a baby girl.  STOP India then helped her return to her family and village. But when she returned&#44; she was not welcomed and the leaders of the village kept abusing her saying she was &quot;spoiling the character of other girls." She and her daughter then fell ill&#44; and out of desperation called Roma&#44; the "mummy" of STOP and Roma arranged for her to come and live at the family home.
 At the family home&#44; Prema met other girls and children rescued from trafficking and Roma helped her begin to love her baby&#44; whom she named Mary.  She says &quot;Today I am a different person.  I feel like a free bird that has just learnt to fly.  I am learning to take responsibilities. I want to be apart of the worthy work that STOP is carrying forward.  I look forward to a happy life for my daughter and want to educate her very well."

