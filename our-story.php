										</strong>
										<p>
											
Mita was trafficked by her neighbor. She says that she &quot;was usually alone at home and grew up as a quiet and lonely child &quot; One day her friend was visiting and her neighbor&#44; Najma invited them to come and eat at her home. Her friend was very hungry and convinced Mita that it was a good idea. They went to the neighbor's home and started to feel "completely out of control&quot;.  She and her friend were drugged.  The girls were then boarded onto a bus. Mita woke the next morning and could not find her friend; Najma responded that "she would never come back". Mita was then picked up by a man and was told that she must entertain him and then he would take her home.  &quot;He came near me and started touching me everywhere.  I cannot put to words as to how I felt at that moment. I shouted and cried out for help."
The next morning the man took her to a house where she saw her friend. &quot;She stood quiet and expressionless for a few minutes and then bursted out crying. She said that she had been raped all night. Finally I realized that we girls were sold as commodities to the vicious desires of customers.  I cried my heart out&#44; but no one heard me. I was brutally beaten up and starved without food for days."
One day&#44; Mita was sent to a man's house.  Some neighbors found out and contacted STOP India.  STOP's president&#44; Roma&#44; along with the police came and rescued her and she directed them to where her friend was.  Both Mita and her friend were rescued.  After giving their testaments&#44; they were taken home to their families.  But the trials were not over.  Najma's son kept his eye on Mita and wanted to harm her.  Eventually her mother sent Mita to STOP's family home where she could be safe&#44; and get an education.  Mita says that she hasn't "really planned anything for the future. I want to be happy and live for today with the hope that tomorrow holds something great for me.&quot;

